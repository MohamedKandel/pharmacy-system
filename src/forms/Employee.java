    private String mylike() {
        return " like '%" + txt_search.getText() + "%'";
    }

    private void search() {
        String txtSer;
        String query = "SELECT ID,Username,Address,Salary,Stat,pro FROM `employee` WHERE ";
        if (rad_ID.isSelected()) {
            txtSer = Tools.getnum(txt_search.getText());
            query += "ID" + mylike();
        } else if (rad_name.isSelected()) {
            txtSer = txt_search.getText();
            query += "Username" + mylike();
        } else if (rad_add.isSelected()) {
            txtSer = txt_search.getText();
            query += "Address" + mylike();
        } else if (rad_stat.isSelected()) {
            txtSer = Tools.removenum(txt_search.getText());
            query += "Stat" + mylike();
        } else if (rad_salary.isSelected()) {
            txtSer = Tools.getnum(txt_search.getText());
            query += "Salary" + mylike();
        } else if (rad_pro.isSelected()) {
            txtSer = Tools.removenum(txt_search.getText());
            query += "pro" + mylike();
        } else {
            String str = ep.getEmployeeByPhone(txt_search.getText());
            //txtSer = Tools.getnum(str);
            query += "ID like '%" + str + "%'";
        }

        e.getCustom(query, tbl);

        if (tbl.getRowCount() <= 0) {
            Tools.msg("Can't Find any information");
            e.selectAll(tbl);
            txt_search.setText("");
        }
    }


