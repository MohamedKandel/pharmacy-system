    private void set() {
        e.setID(Integer.parseInt(txt_ID.getText()));
        e.setUsername(txt_name.getText());
        e.setPass(txt_pass.getText());
        e.setSalary(Double.parseDouble(txt_salary.getText()));
        e.setAdd(txt_address.getText());
        e.setStatus(cmx.getSelectedItem().toString());
        e.setPro(cmx_pro.getSelectedItem().toString());
    }

    private void add() {
        set();
        //==========================================================================
        boolean check = db.database.check(txt_name.getText(), txt_pass.getText());

        if (check) {
            Tools.msg("Can't save employee with this data");
        } else if (check == false) {
            e.add();
            ep.setID(Integer.parseInt(txt_ID.getText()));
            for (int i = 0; i < tblphones.getRowCount(); i++) {
                ep.setPhone(tblphones.getValueAt(i, 0).toString());
                ep.add();
            }
        }
        //==========================================================================
        //e.add();
        clear();
        e.selectAll(tbl);
    }

    private void update() {
        set();
        //==========================================================================

        String newpass = txt_pass.getText();
        String newname = txt_name.getText();
        if (!newpass.equals(oldpass)) {
            String msg = Tools.InputBox("Enter Old Password to apply changes").toString();
            if (msg.equals(oldpass)) {
                e.update();
            } else {
                Tools.msg("Incorrect password");
            }
        } else if (!newname.equals(oldname)) {
            String msg = Tools.InputBox("Enter your Password to apply changes").toString();
            if (msg.equals(oldpass)) {
                e.update();
                ep.setID(Integer.parseInt(txt_ID.getText()));
                //delete all phones for this employee
                ep.deleteAllPhones();
                //add phones again for this employee
                for (int i = 0; i < tblphones.getRowCount(); i++) {
                    ep.setPhone(tblphones.getValueAt(i, 0).toString());
                    ep.add();
                }

            } else {
                Tools.msg("Incorrect password");
            }
        } else {
            e.update();
            ep.setID(Integer.parseInt(txt_ID.getText()));
            //delete all phones for this employee
            ep.deleteAllPhones();
            //add phones again for this employee
            for (int i = 0; i < tblphones.getRowCount(); i++) {
                ep.setPhone(tblphones.getValueAt(i, 0).toString());
                ep.add();
            }
        }
        //==========================================================================
        clear();
        e.selectAll(tbl);
    }

    private void delete() {
        if (Tools.ConfirmMsg("Are you sure you want to delete this employee ?")) {
            ep.setID(Integer.parseInt(txt_ID.getText()));
            ep.deleteAllPhones();
            set();
            e.delete();
            clear();
            e.selectAll(tbl);
        }
    }

    private void addPhones() {
        DefaultTableModel model = (DefaultTableModel) tblphones.getModel();
        model.addRow(new Object[]{txt_phone.getText()});
        txt_phone.setText(null);
        txt_phone.requestFocus();
    }

    private void RemoveAllPhones() {
        DefaultTableModel model = (DefaultTableModel) tblphones.getModel();
        model.setNumRows((0));
    }

    private void RemoveOne() {
        String query = "delete from employee_phones where Phones = '" + txt_phone.getText() + "'";
        db.database.RunNonQuery(query);
        txt_phone.setText("");
        int row = tblphones.getSelectedRow();
        String deleted = tblphones.getValueAt(row, 0).toString();
        ((DefaultTableModel) tblphones.getModel()).removeRow(row);
    }

